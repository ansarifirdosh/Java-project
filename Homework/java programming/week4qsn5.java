public class week4qsn5 //solving by while loop//
{
    public static void main(String[]args)
    {
        int n=4;
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print("1");
            }
            System.out.println();
        }
    }
}

 